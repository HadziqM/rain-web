<section id="signup_discord">
    <h2>公式Discordにて会員登録</h2>

    <ul>
        <li class="center_box">
            <p class="center_box_text">（1）「bot-commands」チャンネル内にあるインターフェイスより、Registerボタンを押します。</p>
            <p class="center_box_img"><img src="/img/ja/articles/signup/discord/discord_1.png" alt="discord_1" /></p>
        </li>
        <li class="center_box">
            <p class="center_box_text">
                （2）以下のようなウィンドウが表示されたら、「Username」及び「Password」欄にそれぞれ自身の設定したい値を入力して、送信ボタンを押してください。<br />送信が完了すると、「account
                successfully created」というメッセージが表示されます。
            </p>
            <p class="center_box_img"><img src="/img/ja/articles/signup/discord/discord_2_1.png" alt="discord_2_1" /></p>
            <p class="center_box_img"><img src="/img/ja/articles/signup/discord/discord_2_2.png" alt="discord_2_2" /></p>
        </li>
        <li class="center_box">
            <p class="center_box_text">
                （3）/card コマンド使用後、空のキャラクターが表示されます。<br />Useボタンを押して、使用キャラクターを確定させると、「successfully switch main
                character」というメッセージが表示されます。<br />これにて会員登録は完了となります。
            </p>
            <p class="center_box_img"><img src="/img/ja/articles/signup/discord/discord_3_1.png" alt="discord_3_1" /></p>
            <p class="center_box_img"><img src="/img/ja/articles/signup/discord/discord_3_2.png" alt="discord_3_2" /></p>
        </li>
    </ul>
</section>
